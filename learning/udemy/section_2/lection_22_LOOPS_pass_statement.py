# The pass statement
# Used to skip over a part of the code while executing the program.

# Usually used as a placeholder at a place
# where some code is required from the syntax side,
# but the code doesn't exist yet.

for i in range(5):  # [0,1,2,3,4]
    if i % 2 == 0:
        pass
    print(i)
